#!/bin/bash

# restore old $PATH value
if [ -n "$OLD_PATH" ]; then
    export PATH=$OLD_PATH
fi
if [ -n "$OLD_LD_LIBPATH"]; then
    export LD_LIBRARY_PATH=$OLD_LD_LIBPATH
fi

unset CC CXX AS LD LDXX LLD STRIP RANLIB OBJDUMP OBJCOPY READELF NM AR PROFDATA CFLAGS CXXFLAGS CPPFLAGS LDFLAGS LDSHARED  PKG_CONFIG_PATH PKG_CONFIG_LIBDIR PKG_CONFIG_SYSTEM_IGNORE_PATH

